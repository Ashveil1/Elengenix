def create_reasoning_engine(client: Any = None) -> VulnReasoningEngine:
    """Create a VulnReasoningEngine with the given client.

    Args:
        client: An LLM client. If None, the engine will use heuristic
            fallback analysis.

    Returns:
        A configured VulnReasoningEngine.
    """
    return VulnReasoningEngine(client=client)
