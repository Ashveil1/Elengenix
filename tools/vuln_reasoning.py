    # Internal helpers
    # ═══════════════════════════════════════════════════════════════════════════

    def _parse_analysis(self, response_text: str) -> AnalysisResult:
        """Parse LLM response into AnalysisResult."""
        data = self._extract_json(response_text)
        if not data:
            return AnalysisResult()

        hypotheses: List[VulnHypothesis] = []
        for h in data.get("hypotheses", []):
            try:
                hypotheses.append(
                    VulnHypothesis(
                        title=h.get("title", ""),
                        vuln_class=h.get("vuln_class", ""),
                        confidence=float(h.get("confidence", 0.5)),
                        reasoning=h.get("reasoning", ""),
                        evidence=h.get("evidence", []),
                        suggested_tests=h.get("suggested_tests", []),
                        cwe=h.get("cwe", ""),
                        severity=h.get("severity", "Medium"),
                        target_endpoint=h.get("target_endpoint", ""),
                        parameter=h.get("parameter", ""),
                        payload=h.get("payload", ""),
                    )
                )
            except Exception as e:
                logger.debug("Failed to parse hypothesis: %s", e)

        return AnalysisResult(
            hypotheses=hypotheses,
            signals=data.get("signals", []),
            next_actions=data.get("next_actions", []),
            risk_assessment=data.get("risk_assessment", ""),
            coverage_gaps=data.get("coverage_gaps", []),
            correlations=data.get("correlations", []),
        )

    @staticmethod
    def _extract_json(text: str) -> Optional[Dict]:
        """Extract JSON from LLM response (handles markdown fences)."""
        try:
            from elengenix.scanning.helpers import extract_json

            result = extract_json(text, expect="object")
            return result if isinstance(result, dict) else None
        except ImportError:
            # Fallback: simple regex extraction
            # Try fenced JSON first
            m = re.search(r"```(?:json)?\s*\n?(.*?)\n?```", text, re.DOTALL)
            if m:
                try:
                    return json.loads(m.group(1))
                except json.JSONDecodeError:
                    pass
            # Try raw JSON
            m = re.search(r"\{[\s\S]*\}", text)
            if m:
                try:
                    return json.loads(m.group(0))
                except json.JSONDecodeError:
                    pass
            return None

    def _heuristic_analysis(
        self,
        target: str,
        tool_name: str,
        tool_output: str,
        previous_findings: List[Dict],
    ) -> AnalysisResult:
        """Fallback heuristic analysis when no LLM client is available.

        Uses pattern matching to find common vulnerability signals.
        """
        signals: List[str] = []
        hypotheses: List[VulnHypothesis] = []
        output_lower = tool_output.lower()

        # Check for common vulnerability signals
        patterns: List[Tuple[str, str, str, float, str]] = [
            (
                r"sql|mysql|postgres|syntax error|union select",
                "sqli",
                "SQL error message detected — possible SQL injection",
                0.6,
                "CWE-89",
            ),
            (
                r"stack trace|traceback|exception|error 500",
                "info_disclosure",
                "Stack trace detected — information disclosure",
                0.5,
                "CWE-209",
            ),
            (
                r"redirect|location:|302|301",
                "open_redirect",
                "Redirect detected — check for open redirect",
                0.4,
                "CWE-601",
            ),
            (
                r"cookie|session|set-cookie",
                "session",
                "Session cookie detected — check for fixation/fixation",
                0.3,
                "CWE-384",
            ),
            (
                r"cors|access-control-allow-origin",
                "cors",
                "CORS header detected — check for misconfiguration",
                0.5,
                "CWE-942",
            ),
            (
                r"jwt|bearer|token",
                "auth",
                "Authentication token detected — check for JWT weaknesses",
                0.4,
                "CWE-287",
            ),
            (
                r"upload|file|attachment",
                "file_upload",
                "File upload endpoint — check for unrestricted upload",
                0.5,
                "CWE-434",
            ),
            (
                r"admin|dashboard|panel",
                "auth_bypass",
                "Admin path detected — check for auth bypass",
                0.5,
                "CWE-285",
            ),
            (
                r"api|json|rest",
                "api_abuse",
                "API endpoint detected — check for rate limiting/IDOR",
                0.4,
                "CWE-639",
            ),
            (
                r"debug|test|dev|staging",
                "info_disclosure",
                "Debug/test endpoint detected — may expose sensitive info",
                0.6,
                "CWE-489",
            ),
        ]

        for pattern, vuln_class, reasoning, confidence, cwe in patterns:
            if re.search(pattern, output_lower):
                signals.append(f"{vuln_class}: pattern detected in {tool_name} output")
                hypotheses.append(
                    VulnHypothesis(
                        title=f"Potential {vuln_class} on {target}",
                        vuln_class=vuln_class,
                        confidence=confidence,
                        reasoning=reasoning,
                        evidence=[f"Pattern matched in {tool_name} output"],
                        suggested_tests=[f"Test {vuln_class} payloads against {target}"],
                        cwe=cwe,
                        severity="Medium" if confidence > 0.4 else "Low",
                        target_endpoint=target,
                    )
                )

        next_actions = list(set(h.suggested_tests[0] for h in hypotheses if h.suggested_tests))[:5]

        return AnalysisResult(
            hypotheses=hypotheses,
            signals=signals,
            next_actions=next_actions,
            risk_assessment=f"Heuristic analysis found {len(hypotheses)} potential issues",
            coverage_gaps=["Full LLM analysis unavailable — install API key for deep reasoning"],
        )

