"""tools/vuln_reasoning.py — Deep LLM Vulnerability Reasoning Engine.

This module makes Elengenix a true AI vulnerability researcher rather than
a simple tool-chaining agent. It leverages the LLM's analytical capabilities
to:

1. Analyse raw tool output and extract security-relevant signals
2. Hypothesise vulnerabilities from patterns the LLM recognises
3. Correlate findings across multiple tools and steps
4. Prioritise which attack vectors to investigate next
5. Generate novel test payloads based on context
6. Reason about business logic flaws from API behaviour

The key insight: a security tool's output is raw data; the LLM's job is to
*interpret* that data, find non-obvious vulnerability patterns, and decide
what to test next — exactly what a human pentester does.
"""

from __future__ import annotations

import json
import logging
import re
import time
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional, Tuple

logger = logging.getLogger("elengenix.vuln_reasoning")


# ═══════════════════════════════════════════════════════════════════════════
# Data structures
# ═══════════════════════════════════════════════════════════════════════════


@dataclass
class VulnHypothesis:
    """A vulnerability hypothesis generated by the LLM."""

    title: str
    vuln_class: str  # e.g. "ssrf", "sqli", "xss", "auth_bypass"
    confidence: float  # 0.0 - 1.0
    reasoning: str
    evidence: List[str] = field(default_factory=list)
    suggested_tests: List[str] = field(default_factory=list)
    cwe: str = ""
    severity: str = "Medium"  # Info/Low/Medium/High/Critical
    target_endpoint: str = ""
    parameter: str = ""
    payload: str = ""


@dataclass
class AnalysisResult:
    """Result of analysing tool output."""

    hypotheses: List[VulnHypothesis] = field(default_factory=list)
    signals: List[str] = field(default_factory=list)
    next_actions: List[str] = field(default_factory=list)
    risk_assessment: str = ""
    coverage_gaps: List[str] = field(default_factory=list)
    correlations: List[str] = field(default_factory=list)


