# VulnReasoningEngine
# ═══════════════════════════════════════════════════════════════════════════


class VulnReasoningEngine:
    """Deep LLM vulnerability reasoning engine.

    This is NOT a tool runner — it's an analytical layer that sits between
    tool execution and action selection. It makes the LLM actively reason
    about what the tools found and what to test next.

    Usage:
        engine = VulnReasoningEngine(client)
        result = engine.analyze_output(
            target="example.com",
            tool_name="ssrf_scanner",
            tool_output="...",
            previous_findings=[...],
            tech_stack={"server": "nginx", "language": "python"},
        )
        for hyp in result.hypotheses:
            print(f"{hyp.title} (confidence={hyp.confidence})")
    """

    def __init__(self, client: Any = None, temperature: float = 0.3) -> None:
        """Initialize the reasoning engine.

        Args:
            client: An LLM client (AIClientManager or UniversalAIClient).
            temperature: LLM temperature for reasoning calls (lower = more
                deterministic, but 0.3 allows some creative hypothesis generation).
        """
        self.client = client
        self.temperature = temperature
        self._analysis_cache: Dict[str, AnalysisResult] = {}

    def analyze_output(
        self,
        target: str,
        tool_name: str,
        tool_output: str,
        previous_findings: Optional[List[Dict]] = None,
        tech_stack: Optional[Dict[str, Any]] = None,
    ) -> AnalysisResult:
        """Analyze raw tool output and generate vulnerability hypotheses.

        This is the core method — it asks the LLM to reason about what the
        tool found, not just parse its output.

        Args:
            target: The target being scanned.
            tool_name: Name of the tool that produced the output.
            tool_output: Raw tool output text.
            previous_findings: Findings from previous steps (for correlation).
            tech_stack: Detected technology stack.

        Returns:
            AnalysisResult with hypotheses, signals, and next actions.
        """
        if not self.client:
            logger.warning("No LLM client — falling back to heuristic analysis")
            return self._heuristic_analysis(target, tool_name, tool_output, previous_findings or [])

        # Truncate output to fit context window
        max_output = 4000
        truncated_output = tool_output[:max_output]
        if len(tool_output) > max_output:
            truncated_output += "\n... [truncated]"

        findings_str = json.dumps(previous_findings or [], indent=2, default=str)[:2000]
        tech_str = json.dumps(tech_stack or {}, indent=2, default=str)

        prompt = _HYPOTHESIS_PROMPT_TEMPLATE.format(
            target=target,
            tool_name=tool_name,
            tool_output=truncated_output,
            previous_findings=findings_str,
            tech_stack=tech_str,
        )

        try:
            from tools.universal_ai_client import AIMessage

            response = self.client.chat(
                [
                    AIMessage(role="system", content=_ANALYSIS_SYSTEM_PROMPT),
                    AIMessage(role="user", content=prompt),
                ],
                temperature=self.temperature,
            )
            return self._parse_analysis(response.content or "")
        except Exception as e:
            logger.error("LLM analysis failed: %s", e)
            return self._heuristic_analysis(target, tool_name, tool_output, previous_findings or [])

    def generate_payloads(
        self,
        vuln_class: str,
        endpoint: str,
        parameter: str,
        tech_stack: Dict[str, Any],
        context: str = "",
        previous_attempts: Optional[List[str]] = None,
    ) -> List[Dict[str, str]]:
        """Generate targeted test payloads for a specific vulnerability class.

        The LLM generates creative, context-aware payloads — not just a
        static wordlist. This adapts to the tech stack and previous results.

        Args:
            vuln_class: e.g. "ssrf", "sqli", "xss", "ssti"
            endpoint: The target endpoint URL.
            parameter: The parameter to test.
            tech_stack: Detected technology stack.
            context: Additional context about the target.
            previous_attempts: Payloads already tried (to avoid repeats).

        Returns:
            List of payload dicts with 'payload', 'technique',
            'expected_behavior', and 'bypass_strategy' keys.
        """
        if not self.client:
            return self._fallback_payloads(vuln_class)

        prompt = _PAYLOAD_GEN_PROMPT.format(
            vuln_class=vuln_class,
            endpoint=endpoint,
            parameter=parameter,
            tech_stack=json.dumps(tech_stack, default=str),
            context=context[:500],
            previous_attempts=json.dumps(previous_attempts or [], default=str)[:500],
        )

        try:
            from tools.universal_ai_client import AIMessage

            response = self.client.chat(
                [
                    AIMessage(role="system", content="You are a payload generation expert."),
                    AIMessage(role="user", content=prompt),
                ],
                temperature=0.4,  # slightly higher for creativity
            )
            data = self._extract_json(response.content or "")
            return data.get("payloads", []) if data else self._fallback_payloads(vuln_class)
        except Exception as e:
            logger.error("Payload generation failed: %s", e)
            return self._fallback_payloads(vuln_class)

    def correlate_findings(
        self,
        all_findings: List[Dict],
        all_hypotheses: List[VulnHypothesis],
        tech_stack: Dict[str, Any],
    ) -> Dict[str, Any]:
        """Correlate findings from multiple tools to find attack chains.

        This is where the LLM's reasoning shines — it can see connections
        between findings that rule-based systems miss.

        Args:
            all_findings: All findings from all tools.
            all_hypotheses: All hypotheses generated so far.
            tech_stack: Detected technology stack.

        Returns:
            Dict with 'chains', 'escalation_paths', and 'combined_risk'.
        """
        if not self.client:
            return {"chains": [], "escalation_paths": [], "combined_risk": "Unknown"}

        findings_str = json.dumps(all_findings[:20], indent=2, default=str)[:3000]
        hyps_str = json.dumps(
            [
                {"title": h.title, "vuln_class": h.vuln_class, "confidence": h.confidence}
                for h in all_hypotheses[:20]
            ],
            indent=2,
        )[:2000]
        tech_str = json.dumps(tech_stack, default=str)

        prompt = _CORRELATION_PROMPT.format(
            all_findings=findings_str,
            all_hypotheses=hyps_str,
            tech_stack=tech_str,
        )

        try:
            from tools.universal_ai_client import AIMessage

            response = self.client.chat(
                [
                    AIMessage(role="system", content=_ANALYSIS_SYSTEM_PROMPT),
                    AIMessage(role="user", content=prompt),
                ],
                temperature=self.temperature,
            )
            return self._extract_json(response.content or "") or {
                "chains": [],
                "escalation_paths": [],
                "combined_risk": "Unknown",
            }
        except Exception as e:
            logger.error("Correlation failed: %s", e)
            return {"chains": [], "escalation_paths": [], "combined_risk": "Unknown"}

    def prioritize_hypotheses(
        self,
        hypotheses: List[VulnHypothesis],
        max_actions: int = 5,
    ) -> List[VulnHypothesis]:
        """Prioritize hypotheses by potential impact and confidence.

        Uses a scoring formula: score = confidence * severity_weight * novelty.
        """
        severity_weights = {
            "Critical": 5.0,
            "High": 4.0,
            "Medium": 2.5,
            "Low": 1.5,
            "Info": 0.5,
        }

        def score(h: VulnHypothesis) -> float:
            sev = severity_weights.get(h.severity, 1.0)
            return h.confidence * sev

        return sorted(hypotheses, key=score, reverse=True)[:max_actions]

