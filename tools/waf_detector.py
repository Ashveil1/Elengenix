"""tools/waf_detector.py — M9: Smart WAF Detector (probe-based).

Detects WAFs (Web Application Firewalls) by sending specific probe
requests and analyzing the response patterns, NOT by matching regex
on headers (which produces false positives from CDN headers like
"server: cloudflare" even when there's no actual WAF behavior).

Probe strategy:
  1. Baseline request (legitimate URL)
  2. Malicious-looking-but-benign payload (e.g. "UNION SELECT NULL--")
  3. SQLi trigger (single quote in param)
  4. XSS trigger (<script> in param)
  5. Path traversal trigger (../../etc/passwd)
  6. Compare responses:
     - If trigger responses are blocked but baseline works = WAF
     - Specific block responses identify specific WAFs:
       - Cloudflare: "Attention Required!" or 403 + cf-ray
       - AWS WAF: "Forbidden" + x-amzn-RequestId
       - Akamai: "Reference #" pattern
       - ModSecurity: "Not Acceptable!" or specific rule IDs
       - Sucuri: "Access Denied - Sucuri Website Firewall"
       - Wordfence: "Your access to this site has been limited"
       - Generic: 403/406/429 with no body diff

Public API:
    WAFSignature  - one detected WAF
    WAFProbeResult - full result of probing
    SmartWAFDetector:
        probe(url) -> WAFProbeResult
        suggest_evasion(waf_signature) -> List[str]
"""

from __future__ import annotations

import hashlib
import logging
import re
import time
import urllib.error
import urllib.parse
import urllib.request
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional, Tuple

logger = logging.getLogger("elengenix.waf_detector")


# ---------------------------------------------------------------------------
# Data classes
# ---------------------------------------------------------------------------


@dataclass
class WAFSignature:
    """One WAF detection signature (block pattern + metadata)."""

    name: str  # "cloudflare", "aws_waf", etc.
    block_status: int  # typical block status
    body_pattern: Optional[str]  # regex of typical block page
    header_patterns: Dict[str, str]  # header -> regex
    confidence_threshold: float = 0.7


@dataclass
class WAFProbeResult:
    """Result of probing a target for WAF presence."""

    target: str
    waf_detected: bool
    waf_name: str  # "cloudflare" / "none" / "unknown"
    confidence: float
    blocked_payloads: List[str]  # which probes got blocked
    passed_payloads: List[str]  # which probes passed (WAF bypassed)
    signature_hits: List[str]  # which signatures matched
    baseline_status: int
    suggested_evasions: List[str] = field(default_factory=list)
    raw_responses: Dict[str, Dict[str, Any]] = field(default_factory=dict)


